class CrossEntropyModule(object):
    """
    Cross entropy loss module.
    """

    def forward(self, x, y):
        """
        Forward pass.
        Args:
          x: input to the module
          y: labels of the input
        Returns:
          out: cross entropy loss

        TODO:
        Implement forward pass of the module.
        """

        #######################
        # PUT YOUR CODE HERE  #
        #######################
        L_ = -np.log(np.take_along_axis(x, np.array([y]).T, axis=1))
        out = np.mean(L_)
        #######################
        # END OF YOUR CODE    #
        #######################

        return out

    def backward(self, x, y):
        """
        Backward pass.
        Args:
          x: input to the module
          y: labels of the input
        Returns:
          dx: gradient of the loss with the respect to the input x.

        TODO:
        Implement backward pass of the module.
        """

        #######################
        # PUT YOUR CODE HERE  #
        #######################
        ydivx = np.zeros_like(x)
        xs = np.take_along_axis(x, np.array([y]).T, axis=1)
        np.put_along_axis(ydivx, np.array([y]).T, 1 / xs, axis=1)
        dx = -(1 / ydivx.shape[0]) * ydivx
        #######################
        # END OF YOUR CODE    #
        #######################

        return dx